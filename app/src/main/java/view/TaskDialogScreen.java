    public Boolean checkFields(){
        Boolean check = true;
        hideErrorsField();
        if(jTextFieldName.getText().isEmpty()){
             jLabelNameRequired.setVisible(true);
             check = false;
        }
        if(jFormattedTextFieldDeadline.getText().isEmpty()){
            jLabelDeadlineRequired.setVisible(true);
             check = false;
        }
        return check;
    }

